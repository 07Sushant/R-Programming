# Data Types in R - Programming

# Logical Datatype
a <- TRUE
a1 <- FALSE
print(class(a))
print(class(a1))

# Numeric Datatypes

b<-10.5
print(class(b))
print(typeof(b))

b1 <-23
print(class(b1))

# Characters Datatypes
b2<-"Hello"
print(class(b2))

# Integers Datatypes
b3 <- 10L
print(class(b3))

# Complex Datatypes
c1 <- 6+3i
print(class(c1))

